let bookmark1 = document.querySelector("#bookmark1");
let bookmarkBlack1 = document.querySelector("#bookmarkBlack1");

bookmark1.addEventListener("click", () =>{
    if(bookmark1.id === ("bookmark1")){
        bookmark1.classList.add("ON");
        bookmarkBlack1.classList.add("ON");
    }
})

bookmarkBlack1.addEventListener("click", () =>{
    if(bookmarkBlack1.id === ("bookmarkBlack1")){
        bookmarkBlack1.classList.remove("ON");
        bookmark1.classList.remove("ON");
    }
})
